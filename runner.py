def material_swap_worth(cfg, mat_a, mat_b, threads=None):
    """Reactivity worth of replacing material id mat_a by mat_b everywhere
    (including every axial zone in 3-D)."""
    base = run_case(cfg, threads=threads)
    cfg2 = copy.deepcopy(cfg)
    cfg2["blocks"] = [[mat_b if v == mat_a else v for v in row]
                      for row in cfg["blocks"]]
    if cfg2.get("axial"):
        for z in cfg2["axial"]["zones"]:
            z["blocks"] = [[mat_b if v == mat_a else v for v in row]
                           for row in z["blocks"]]
    pert = run_case(cfg2, threads=threads)
    k1, k2 = base["keff"], pert["keff"]
    return dict(k1=k1, k2=k2,
                rho1_pcm=rho_pcm(k1), rho2_pcm=rho_pcm(k2),
                worth_pcm=rho_pcm(k2) - rho_pcm(k1),
                base=base, pert=pert)


def critical_boron_search(cfg, target=1.0, tol_pcm=2.0, max_runs=25,
                          threads=None, callback=None):
    """Find the uniform soluble-boron concentration [ppm] that makes
    k_eff = target.  Every material carrying `designer` metadata (made by
    xslib) is rebuilt at the trial ppm; other materials stay untouched."""
    import xslib
    des = [i for i, m in enumerate(cfg["materials"]) if "designer" in m]
    if not des:
        raise ValueError("no designer materials in the core "
                         "(build fuels in the Fuel designer tab first)")

    def k_of(ppm):
        c = copy.deepcopy(cfg)
        for i in des:
            d = cfg["materials"][i]["designer"]
            nm = xslib.cell_from_N(d["N"], ppm, d["rho_mod"],
                                   d["r_fuel"], d["pitch"],
                                   enrich_label=d.get("enrich"))
            nm["name"] = cfg["materials"][i]["name"]
            c["materials"][i] = nm
        return run_case(c, threads=threads)["keff"]

    history = []

    def probe(ppm):
        k = k_of(ppm)
        history.append((ppm, k))
        if callback:
            callback(len(history), ppm, k)
        return k

    lo, hi = 0.0, 1000.0
    klo = probe(lo)
    if klo < target:
        return dict(history=history, ppm=0.0, k=klo, runs=len(history),
                    note="core is below target even at 0 ppm")
    khi = probe(hi)
    while khi > target and len(history) < max_runs:
        lo, klo = hi, khi
        hi *= 2.0
        if hi > 40000:
            raise RuntimeError("not reaching target even at 40000 ppm")
        khi = probe(hi)
    while len(history) < max_runs:
        mid = 0.5 * (lo + hi)
        k = probe(mid)
        if abs(rho_pcm(k) - rho_pcm(target)) < tol_pcm:
            return dict(history=history, ppm=mid, k=k, runs=len(history))
        if k > target:
            lo = mid
        else:
            hi = mid
    ppm, k = history[-1]
    return dict(history=history, ppm=ppm, k=k, runs=len(history))


def criticality_search(cfg, mat_ids, group=None, target=1.0,
                       tol_pcm=1.0, max_runs=25, threads=None, callback=None):
    """Find the uniform delta-Sigma_a (added to `group` of materials
    `mat_ids`) that makes k_eff = target. Bisection with automatic
    bracketing. Returns dict(history=[(dsa,k),...], dsa=..., k=...)."""
    ng = cfg["ng"]
    if group is None:
        group = ng
    gi = group - 1

    def k_of(dsa):
        c = copy.deepcopy(cfg)
        for mid in mat_ids:
            c["materials"][mid - 1]["Sa"][gi] += dsa
            if c["materials"][mid - 1]["Sa"][gi] <= 0:
                raise ValueError("Sigma_a became non-positive during search")
        r = run_case(c, threads=threads)
        return r["keff"]

    history = []

    def probe(dsa):
        k = k_of(dsa)
        history.append((dsa, k))
        if callback:
            callback(len(history), dsa, k)
        return k

    k0 = probe(0.0)
    if abs(rho_pcm(k0) - rho_pcm(target)) < tol_pcm:
        return dict(history=history, dsa=0.0, k=k0, runs=len(history))

    lo, hi = None, None
    if k0 > target:
        step = 0.5 * min(m["Sa"][gi] for m in
                         (cfg["materials"][i - 1] for i in mat_ids))
        step = max(step, 1e-4)
        d = step
        klo, dlo = k0, 0.0
        while len(history) < max_runs:
            k = probe(d)
            if k < target:
                lo, hi = dlo, d
                break
            dlo, klo = d, k
            d *= 2.0
        else:
            raise RuntimeError("could not bracket the critical point")
    else:
        samin = min(cfg["materials"][i - 1]["Sa"][gi] for i in mat_ids)
        d = -0.25 * samin
        dhi, khi = 0.0, k0
        while len(history) < max_runs:
            k = probe(d)
            if k > target:
                lo, hi = d, dhi
                break
            dhi, khi = d, k
            if abs(d) > 0.85 * samin:
                raise RuntimeError("critical point not reachable "
                                   "(Sigma_a would become negative)")
            d = max(d * 2.0, -0.85 * samin)
        else:
            raise RuntimeError("could not bracket the critical point")

    while len(history) < max_runs:
        mid = 0.5 * (lo + hi)
        k = probe(mid)
        if abs(rho_pcm(k) - rho_pcm(target)) < tol_pcm:
            return dict(history=history, dsa=mid, k=k, runs=len(history))
        if k > target:
            lo = mid
        else:
            hi = mid
    dsa, k = history[-1]
    return dict(history=history, dsa=dsa, k=k, runs=len(history))
