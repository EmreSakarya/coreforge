"""CoreForge — Python driver for the Fortran engine.

Writes the keyword input file, launches ``solver/coreforge``, parses the
summary and the field CSVs (2-D or 3-D), and provides the higher-level
reactor-physics tools (material-swap worth, criticality searches).

Only numpy is required here; the Streamlit app adds pandas/plotly on top.
"""
import copy
import os
import shutil
import subprocess
import tempfile
import time

import numpy as np

HERE = os.path.dirname(os.path.abspath(__file__))
BC_CODE = {"vacuum": 0, "reflective": 1}


def ncpu():
    """CPU count that respects container/cgroup limits (Streamlit Cloud
    boxes report the host's cores via cpu_count but only a couple are
    actually schedulable)."""
    try:
        return len(os.sched_getaffinity(0)) or 1
    except AttributeError:          # non-Linux
        return os.cpu_count() or 1


# ----------------------------------------------------------------------
def build_engine():
    """One-shot engine build from source.  Used when only the source ships
    (e.g. Streamlit Community Cloud, where packages.txt installs gfortran).
    Tries ifx first (the development compiler), then gfortran.  Each call
    compiles in its own private temp directory (so concurrent sessions
    never share the object/module files or a half-written binary) and the
    finished binary is moved into place atomically.  Returns the path or
    None; the compiler log is written to solver/build.log either way."""
    sdir = os.path.join(HERE, "solver")
    src = os.path.join(sdir, "coreforge.f90")
    out = os.path.join(sdir, "coreforge")
    if not os.path.isfile(src):
        return None
    for fc, flags in (("ifx", ["-O3", "-qopenmp"]),
                      ("gfortran", ["-O3", "-fopenmp"])):
        if shutil.which(fc) is None:
            continue
        tdir = tempfile.mkdtemp(prefix="build-", dir=sdir)
        try:
            try:
                r = subprocess.run([fc, *flags, src, "-o", "coreforge"],
                                   cwd=tdir, capture_output=True, text=True,
                                   timeout=300)
                logtxt = (f"$ {fc} {' '.join(flags)} coreforge.f90\n"
                          f"{r.stdout}\n{r.stderr}\n")
                built = r.returncode == 0
            except subprocess.TimeoutExpired:
                logtxt = (f"$ {fc} {' '.join(flags)} coreforge.f90\n"
                          "compiler timed out after 300 s\n")
                built = False
            logf = os.path.join(tdir, "build.log")
            with open(logf, "w") as f:
                f.write(logtxt)
            os.replace(logf, os.path.join(sdir, "build.log"))
            tmp = os.path.join(tdir, "coreforge")
            if built and os.path.isfile(tmp):
                os.chmod(tmp, 0o755)
                os.replace(tmp, out)
                return out
        finally:
            shutil.rmtree(tdir, ignore_errors=True)
    return None


def engine_path(autobuild=True):
    for cand in ("coreforge", "coreforge.exe", "coreforge.out"):
        p = os.path.join(HERE, "solver", cand)
        if os.path.isfile(p) and os.access(p, os.X_OK):
            return p
    return build_engine() if autobuild else None


def expand_blocks(blocks, div):
    """Block lattice (rows top-first) -> fine mesh map (rows top-first)."""
    a = np.asarray(blocks, dtype=int)
    return np.kron(a, np.ones((div, div), dtype=int))


def fissile_ids(materials):
    return [i + 1 for i, m in enumerate(materials)
            if any(v > 0 for v in m["nuSf"])]


def rho_pcm(k):
    return (k - 1.0) / k * 1e5


def auto_threads(cfg):
    """Thread count matched to problem size: the engine parallelises over
    mesh rows, so tiny meshes are FASTER on few threads (fork/barrier
    overhead dominates)."""
    rows = len(cfg["blocks"]) * cfg["div"]
    if cfg.get("axial"):
        nzl = sum(int(z["layers"]) for z in cfg["axial"]["zones"])
        rows *= max(1, nzl * int(cfg["axial"].get("divz", 1)))
    return max(1, min(ncpu(), rows // 12 + 1))


# ----------------------------------------------------------------------
def write_input(cfg):
    """cfg keys: ng, pitch, div, blocks, materials, bc (4 names for 2-D,
    6 for 3-D: W,E,S,N,Bottom,Top), gamma, optional omega/tolk/tols/
    ninner/maxout, and optional `axial` for 3-D:
      axial = dict(dz=..., divz=1, zones=[{label, layers, blocks}, ...])
    (zones bottom -> top; every zone's block lattice must have the same
    shape as cfg['blocks'])."""
    ng = cfg["ng"]
    axial = cfg.get("axial")
    fine = expand_blocks(cfg["blocks"], cfg["div"])
    nyf, nxf = fine.shape
    d = cfg["pitch"] / cfg["div"]
    L = []
    L.append(f"# CoreForge input — generated by runner.py")
    L.append(f"NG {ng}")
    L.append(f"NX {nxf}")
    L.append(f"NY {nyf}")
    if axial:
        divz = int(axial.get("divz", 1))
        nzl = sum(int(z["layers"]) for z in axial["zones"]) * divz
        L.append(f"NZ {nzl}")
        L.append(f"DZ {axial['dz']/divz:.10g}")
    L.append(f"DX {d:.10g}")
    L.append(f"DY {d:.10g}")
    L.append("BC " + " ".join(str(BC_CODE[b]) for b in cfg["bc"]))
    L.append(f"GAMMA {cfg.get('gamma', 0.4692):.10g}")
    for key in ("omega", "tolk", "tols", "ninner", "maxout"):
        if key in cfg and cfg[key] is not None:
            L.append(f"{key.upper()} {cfg[key]:.10g}" if isinstance(cfg[key], float)
                     else f"{key.upper()} {cfg[key]}")
    mats = cfg["materials"]
    L.append(f"NMAT {len(mats)}")
    for mid, m in enumerate(mats, start=1):
        L.append(f"MAT {mid}   # {m.get('name','')}")
        L.append("D    " + " ".join(f"{v:.10g}" for v in m["D"]))
        L.append("SA   " + " ".join(f"{v:.10g}" for v in m["Sa"]))
        L.append("NUSF " + " ".join(f"{v:.10g}" for v in m["nuSf"]))
        L.append("CHI  " + " ".join(f"{v:.10g}" for v in m["chi"]))
        L.append("SCAT")
        for gf in range(ng):
            L.append(" ".join(f"{m['scat'][gf][gt]:.10g}" for gt in range(ng)))
    if axial:
        divz = int(axial.get("divz", 1))
        l1 = 1
        for z in axial["zones"]:
            nl = int(z["layers"]) * divz
            zf = expand_blocks(z["blocks"], cfg["div"])
            if zf.shape != fine.shape:
                raise ValueError("all axial zones must share the radial "
                                 "lattice shape")
            L.append(f"MAP {l1} {l1+nl-1}   # {z.get('label','zone')}")
            for row in zf:
                L.append(" ".join(str(int(v)) for v in row))
            l1 += nl
    else:
        L.append("MAP")
        for row in fine:
            L.append(" ".join(str(int(v)) for v in row))
    return "\n".join(L) + "\n"


