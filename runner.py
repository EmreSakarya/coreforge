def _parse_summary(stdout):
    out = {}
    bal, inbal = [], False
    for line in stdout.splitlines():
        s = line.strip()
        if s.startswith("BALANCE_BEGIN"):
            inbal = True
            continue
        if s.startswith("BALANCE_END"):
            inbal = False
            continue
        if inbal:
            bal.append(s)
            continue
        for key, cast in (("KEFF", float), ("FXY", float), ("DR", float),
                          ("TIME_S", float), ("OUTERS", int)):
            if s.startswith(key + " ") or s.startswith(key + "="):
                out[key.lower()] = cast(s.split("=")[1])
        if s.startswith("CONVERGED"):
            out["converged"] = "YES" in s
        if s.startswith("FXYLOC"):
            out["fxy_loc"] = tuple(int(v) for v in s.split("=")[1].split())
    if bal:
        hdr = bal[0].split(",")
        rows = [[float(v) for v in r.split(",")] for r in bal[1:]]
        out["balance"] = {"columns": hdr, "rows": rows}
    return out


def _display_zone(cfg):
    """Zone index used for the radial material display (most fissile)."""
    fids = set(fissile_ids(cfg["materials"]))
    best, bestn = 0, -1
    for zi, z in enumerate(cfg["axial"]["zones"]):
        n = sum(1 for row in z["blocks"] for v in row if v in fids)
        if n > bestn:
            best, bestn = zi, n
    return best


def _prune_runs(runs, keep=200):
    """Keep only the most recent `keep` run directories so a long-lived
    server (e.g. Streamlit Cloud) doesn't grow runs/ without bound."""
    try:
        dirs = [os.path.join(runs, d) for d in os.listdir(runs)]
        dirs = [d for d in dirs if os.path.isdir(d)]
        if len(dirs) <= keep:
            return
        dirs.sort(key=lambda d: os.path.getmtime(d))
        for d in dirs[:len(dirs) - keep]:
            shutil.rmtree(d, ignore_errors=True)
    except OSError:
        pass


def run_case(cfg, workdir=None, threads=None, timeout=1800):
    """Run one eigenvalue case. Returns a dict with keff, fxy, balance,
    flux, power, coordinates, log, workdir.  For 3-D cases (cfg['axial'])
    also phi3d (ng,nz,ny,nx), power3d, axial_power, z — while the 2-D
    fields phi/power hold the axial averages so every 2-D display works."""
    eng = engine_path()
    if eng is None:
        raise RuntimeError(
            "Engine not found and auto-build failed (see solver/build.log).\n"
            "Build it manually with one of:\n"
            "  ifx -O3 -qopenmp solver/coreforge.f90 -o solver/coreforge\n"
            "  gfortran -O3 -fopenmp solver/coreforge.f90 -o solver/coreforge")
    if workdir is None:
        # Streamlit serves every session from ONE process, so a pid+second
        # name collides across concurrent users (silently overwriting each
        # other's flux.csv/input.txt).  mkdtemp guarantees a unique dir.
        runs = os.path.join(HERE, "runs")
        os.makedirs(runs, exist_ok=True)
        _prune_runs(runs)
        workdir = tempfile.mkdtemp(prefix=time.strftime("%Y%m%d-%H%M%S-"),
                                   dir=runs)
    os.makedirs(workdir, exist_ok=True)
    text = write_input(cfg)
    with open(os.path.join(workdir, "input.txt"), "w") as f:
        f.write(text)

    env = dict(os.environ)
    env["OMP_NUM_THREADS"] = str(threads or auto_threads(cfg))
    try:
        proc = subprocess.run([eng, "input.txt"], cwd=workdir, env=env,
                              capture_output=True, text=True, timeout=timeout)
    except subprocess.TimeoutExpired:
        raise RuntimeError(
            f"Engine timed out after {timeout} s — the mesh is likely too "
            "fine for this machine.  Reduce div/divz and try again.")
    if proc.returncode != 0:
        raise RuntimeError("Engine failed:\n" + proc.stderr + "\n" + proc.stdout[-2000:])

    res = _parse_summary(proc.stdout)
    res["log"] = proc.stdout
    res["input_text"] = text
    res["workdir"] = workdir
    with open(os.path.join(workdir, "run.log"), "w") as f:  # QA trail
        f.write(proc.stdout)
        if proc.stderr:
            f.write("\n--- stderr ---\n" + proc.stderr)

    fine = expand_blocks(cfg["blocks"], cfg["div"])
    nyf, nxf = fine.shape
    d = cfg["pitch"] / cfg["div"]
    axial = cfg.get("axial")
    if axial:
        divz = int(axial.get("divz", 1))
        nzf = sum(int(z["layers"]) for z in axial["zones"]) * divz
        dzf = axial["dz"] / divz
    else:
        nzf, dzf = 1, 1.0

    flux = np.genfromtxt(os.path.join(workdir, "flux.csv"),
                         delimiter=",", names=True)
    power = np.genfromtxt(os.path.join(workdir, "power.csv"),
                          delimiter=",", names=True)
    ng = cfg["ng"]
    # engine writes x fastest, then y (bottom-first), then z (bottom-first)
    phi3d = np.stack([flux[f"phi_g{g}"].reshape(nzf, nyf, nxf)
                      for g in range(1, ng + 1)])          # [g,z,y,x]
    pow3d = power["power_rel"].reshape(nzf, nyf, nxf)
    res["nz"] = nzf
    res["phi3d"] = phi3d
    res["power3d"] = pow3d
    res["phi"] = phi3d.mean(axis=1)                        # [g,y,x]
    res["power"] = pow3d.mean(axis=0)                      # [y,x]
    if axial:
        zone = axial["zones"][_display_zone(cfg)]
        res["mat_fine"] = np.flipud(expand_blocks(zone["blocks"], cfg["div"]))
        lp = pow3d.sum(axis=(1, 2))                        # per-layer power
        nzp = lp > 1e-12
        res["axial_power"] = np.where(nzp, lp / max(lp[nzp].mean(), 1e-30),
                                      0.0)
        res["z"] = (np.arange(nzf) + 0.5) * dzf
        res["fz"] = float(res["axial_power"].max()) if nzp.any() else 1.0
    else:
        res["mat_fine"] = np.flipud(fine)
    res["x"] = (np.arange(nxf) + 0.5) * d
    res["y"] = (np.arange(nyf) + 0.5) * d
    res["dx"] = d
    return res


# ----------------------------------------------------------------------
def block_powers(res, cfg):
    """Aggregate the fine power map back to the block lattice.
    Returns (nby,nbx) array, top-first, normalised to mean 1 over fuel
    blocks; non-fuel blocks are NaN."""
    div = cfg["div"]
    p = np.flipud(res["power"])                    # top-first
    nby, nbx = np.asarray(cfg["blocks"]).shape
    bp = p.reshape(nby, div, nbx, div).sum(axis=(1, 3))
    fuel = bp > 1e-12
    if fuel.any():
        bp = bp / bp[fuel].mean()
    out = np.where(fuel, bp, np.nan)
    return out


